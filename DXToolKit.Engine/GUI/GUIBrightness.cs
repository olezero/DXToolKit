namespace DXToolKit.Engine {
	/// <summary>
	/// Defines a standard set of brightness levels used by the GUI
	/// </summary>
	public enum GUIBrightness {
		/// <summary>
		/// Darkest brightness based on GUIColorPaletteDescription
		/// </summary>
		Darkest = 0,

		/// <summary>
		/// Dark brightness based on GUIColorPaletteDescription
		/// </summary>
		Dark = 1,

		/// <summary>
		/// Normal brightness based on GUIColorPaletteDescription
		/// </summary>
		Normal = 2,

		/// <summary>
		/// Bright brightness based on GUIColorPaletteDescription
		/// </summary>
		Bright = 3,

		/// <summary>
		/// Brightest brightness based on GUIColorPaletteDescription
		/// </summary>
		Brightest = 4
	}
}